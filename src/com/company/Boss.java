package com.company;

public class Boss {
    private int bossHealth;
    private int bossesDamage;
    private String bossesSuper;


    public int getBossHealth() {
        return bossHealth;
    }

    public void setBossHealth(int bossHealth) {
        this.bossHealth = bossHealth;
    }

    public int getBossesDamage() {
        return bossesDamage;
    }

    public void setBossesDamage(int bossesDamage) {
        this.bossesDamage = bossesDamage;
    }

    public String getBossesSuper() {
        return bossesSuper;
    }

    public void setBossesSuper(String bossesSuper) {
        this.bossesSuper = bossesSuper;
    }
}
